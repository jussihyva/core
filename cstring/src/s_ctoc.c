#include "../inc/cstr.h"

void	s_ctoc(char *str, int from, int to)
{
	size_t		i;

	i = s_len(str);
	while (i--)
	{
		if (str[i] == from)
			str[i] = to;
	}
}

/*
**  ----------------------------------------------------------------------------
**
**	S_ctoc
**
**	Convert all occurences of `from` to `to` in the string `str`.
**
**  ----------------------------------------------------------------------------
*/
