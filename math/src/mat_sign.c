#include "../../inc/core.h"

int					math_sign(int64_t n)
{
	if (n != 0)
		return (n > 0 ? 1 : -1);
	else
		return (0);
}

/*
**  ----------------------------------------------------------------------------
**
**	math_sign
**
**	Return the sign of `n`. If `n` is zero returns 0.
**
**  ----------------------------------------------------------------------------
*/
