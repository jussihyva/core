#include "../../../inc/core.h"

t_ret	parr_del_last(t_parray *arr)
{
	t_ret	ret;

	ret = parr_del(arr, arr->len - 1);
	if (ret < 0)
		return (ret);
	return (CR_SUCCESS);
}

