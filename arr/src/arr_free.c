#include "../inc/arr.h"

ssize_t		arr_free(t_arr *arr)
{
	if (arr_null(arr))
		return (ARR_FAIL);
	free(arr->data);
	*arr = (t_arr){NULL, 0, 0, 0};
	return (ARR_SUCCESS);
}

