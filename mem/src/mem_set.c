#include "../inc/mem.h"

void		*mem_set(void *s, int c, size_t n)
{
	uint8_t	*ptr;

	ptr = s;
	while (n)
	{
		*ptr = c;
		ptr++;
		n--;
	}
	return (s);
}

/*
**  ----------------------------------------------------------------------------
**
**	mem_set
**
**	Void memory set; sets `n` bytes of memory pointed to by `s` with value `c`.
**
**  ----------------------------------------------------------------------------
*/
